SELECT TRIM(T2.POLICY_CODE) AS POLICY_CODE,
       TRIM(T1.CONTNO) AS CONTNO,
       T2.APPLY_AMOUNT AS APPLY_AMOUNT,
       T1.MONEY AS MONEY
       (CASE
         WHEN (T2.APPLY_AMOUNT - T1.MONEY+=0 THEN
          '否'
         ELSE
          '是'
       END) AS RESULT1
  FROM TMP_LIS_1003_GB T1
  FULL JOIN TMP_NCS_1003_GB@DB_185 T2
    ON TRIM(T1.CONTNO) = TRIM(T2.POLICY_CODE)
 ORDER BY T2.POLICY_CODE;

                       
