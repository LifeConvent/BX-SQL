SELECT COUNT(1) 笔数,SUM(MONEY) 价格
  FROM LCINSUREACCTRACE A
 WHERE A.MAKEDATE = DATE '2016-09-21'
   AND A.MONEYTYPE IN ('BF', 'AM', 'TB')
   AND A.CONTNO IN (SELECT CONTNO
                      FROM LCCONT B
                     WHERE B.CONTTYPE = '1'
                       AND B.APPFLAG IN ('1', '4')
                       AND B.MANAGECOM LIKE '8647%');
                       
