       ON A.POLICY_ID = B.POLICY_ID
 WHERE A.FUND_PROCESS_STATUS = '1' --交易处理状态
   AND B.ORGAN_CODE LIKE '8647%' --机构代码
   AND TO_CHAR(A.UPDATE_TIME, 'YYYY-MM-DD') = '2016-09-21';
   
